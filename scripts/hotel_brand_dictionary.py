# TourAPI에 등록되지 않은 호텔의 수동 매핑
# 키: 원본 호텔명 (정확히 일치해야 함)
# 값: TourAPI contentId 문자열 또는 None (매칭 없음 확정)
MANUAL_MAPPING = {
    # 진단 결과: TourAPI에 "인터컨티넨탈 서울 코엑스" 별도 등록 없음 확인
    "인터컨티넨탈 서울 코엑스": None,
    # 조선팰리스강남은 normalize_candidate_title 로직 적용으로 자동 매칭 복지 → 수동 매핑 제거
}


# 호텔명 정규화 규칙 (전처리 단계)
NAME_NORMALIZATION = [
    ("（", "("),   # 전각 괄호 → 반각
    ("）", ")"),
    ("·", " "),    # 중간점 → 공백
    ("∙", " "),
    ("-", " "),    # 하이픈 → 공백
    ("  ", " "),   # 이중 공백
]


def normalize_hotel_name(name: str) -> str:
    """호텔명 기본 정규화 + 법인 접두사 제거"""
    result = name.strip()

    # 법인 접두사 제거 (앞쪽)
    # 예: "(주)호텔신라" → "호텔신라", "㈜조선호텔" → "조선호텔"
    result = re.sub(r'^\s*[(（]\s*(주|유|재|사|합명|합자|합)\s*[)）]\s*', '', result)
    result = re.sub(r'^\s*㈜\s*', '', result)
    result = re.sub(r'^\s*㈘\s*', '', result)
    # 뒤쪽 법인 표기 제거: "조선호텔(주)" → "조선호텔"
    result = re.sub(r'\s*[(（]\s*(주|유|재)\s*[)）]\s*$', '', result)
    # "주식회사" 문자열 제거
    result = re.sub(r'\s*주식회사\s*', '', result)

    for find, replace in NAME_NORMALIZATION:
        result = result.replace(find, replace)
    result = re.sub(r'\s+', ' ', result).strip()
    return result


def apply_brand_dictionary(name: str) -> str:
    """
    호텔명에서 브랜드 사전에 등록된 붙여쓰기를 공백 있는 형태로 변환.
    가장 긴 브랜드명부터 매칭 시도 (greedy longest match).

    예: "파크하얏트서울"  → "파크 하얏트 서울"
        "조선팰리스강남"  → "조선 팰리스 강남"
        "오크우드프리미어" → "오크우드 프리미어"
    """
    sorted_brands = sorted(
        BRAND_DICTIONARY.keys(),
        key=lambda x: len(x),
        reverse=True
    )

    result = name
    for compact_brand in sorted_brands:
        if compact_brand in result:
            spaced_brand = BRAND_DICTIONARY[compact_brand]
            result = result.replace(compact_brand, spaced_brand)
            result = re.sub(r'\s+', ' ', result).strip()

    return result


def get_manual_mapping(hotel_name: str):
    """
    수동 매핑 테이블에서 호텔 조회.

    Returns:
        (found: bool, content_id: Optional[str])
        found=True, content_id=None    → 매칭 불가로 확정됨
        found=True, content_id="xxx"   → 직접 지정된 contentId
        found=False, content_id=None   → 수동 매핑 없음 (자동 검색 진행)
    """
    normalized = normalize_hotel_name(hotel_name)

    if normalized in MANUAL_MAPPING:
        return True, MANUAL_MAPPING[normalized]
    if hotel_name.strip() in MANUAL_MAPPING:
        return True, MANUAL_MAPPING[hotel_name.strip()]

    return False, None


def normalize_candidate_title(title: str) -> str:
    """
    TourAPI 후보 호텔명을 비교용으로 정규화.

    수행 작업:
    1. 괄호 및 내용 제거 - "(Josun Palace, a Luxury...)" 통째로 제거
    2. 쉼표 이후 부가 설명 제거 - ", 럭셔리 콜렉션 호텔" 제거
    3. 공통 접미사 제거 - "호텔", "리조트" 등
    4. 순수 영문 단어 구간 제거 - "Seoul Gangnam" 같은 순 영문 단어 제거
    5. 공백 정규화

    예:
    입력: "조선 팰리스 서울 강남, 럭셔리 콜렉션 호텔 (Josun Palace, a Luxury Collection Hotel)"
    출력: "조선 팰리스 서울 강남"

    입력: "그랜드 하얏트 인천 호텔 (Grand Hyatt Incheon)"
    출력: "그랜드 하얏트 인천"
    """
    result = title.strip()

    # 1. 괄호 및 괄호 안 내용 제거 (꼽은괄호, 소괄호 모두)
    result = re.sub(r'\([^)]*\)', ' ', result)
    result = re.sub(r'\[[^\]]*\]', ' ', result)
    result = re.sub(r'（[^）]*）', ' ', result)  # 전각 괄호

    # 2. 쉼표 이후 부가 설명 제거
    if ',' in result:
        result = result.split(',')[0].strip()
    if '、' in result:  # 일본어 쉼표
        result = result.split('、')[0].strip()

    # 3. 공통 접미사 제거
    for suffix in ['호텔', '리조트', 'Hotel', 'HOTEL', 'Resort', 'RESORT']:
        result = re.sub(
            rf'\s*{re.escape(suffix)}\s*$',
            '',
            result,
            flags=re.IGNORECASE
        )

    # 4. 한글 뒤에 나오는 순수 영문 단어 구간 제거
    result = re.sub(r'([\uAC00-\uD7A3])\s+([A-Za-z][A-Za-z\s]*[A-Za-z])$', r'\1', result)

    # 5. 공백 정규화
    result = re.sub(r'\s+', ' ', result).strip()

    return result
